#include "libft.h"

size_t			ft_list_size(t_list *begin_list)
{
	t_list		*a;
	size_t		nb;

	nb = 0;
	a = begin_list;
	while (a)
	{
		nb++;
		a = a->next;
	}
	return (nb + 1);
}
